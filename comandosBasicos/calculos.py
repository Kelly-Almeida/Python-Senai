# Calculos em Python
print("------------- Calculos de Python -------------")
print("\n==== Soma ====")
print(f"2 + 2 = {2 + 2}")

print("\n==== Subtração ====")
print(f"2 - 2 = {2 - 2}")

print("\n==== Multiplicação ====")
print(f"2 * 2 = {2 * 2}")

print("\n==== Divisão ====")
print(f"2 / 2 = {2 / 2}")

print("\n==== Módulo ====")
print(f"2 % 2 = {2 % 2}")

print("\n==== Exponenciação ====")
print(f"2 ** 2 = {2**2}")